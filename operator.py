# and, or, not
print(1 != 3) # 같지 않음
print(not (1 != 3)) # 같지 않음의 반대는 같음
print((3 > 0) and (3 < 5))
print((3 > 0) & (3 < 5))

print((3 > 0) or (3 > 5))
print((3 > 0) | (3 > 5))

