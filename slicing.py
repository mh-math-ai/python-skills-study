jumin = "990120-1234567"
print("성별 : " + jumin[7]) # 문자열에서 특정 위치의 문자를 가져올 때는 인덱싱을 사용합니다.
# 인덱스는 0부터 시작하며, 음수 인덱스는 문자열의 끝에서부터 역으로 접근합니다.
print("연 : " + jumin[0:2]) # 문자열에서 특정 범위의 문자를 가져올 때는 슬라이싱을 사용합니다.
# 슬라이싱은 [시작 인덱스:끝 인덱스]의 형태로 사용하며,
# 시작 인덱스는 포함되고 끝 인덱스는 포함되지 않습니다.
print("월 : " + jumin[2:4])
print("일 : " + jumin[4:6])
print("생년월일 : " + jumin[0:6])
print("생년월일 : " + jumin[:6]) # 시작 인덱스를 생략하면 문자열의 처음부터 시작합니다.
print("뒤 7자리 : " + jumin[7:14])
print("뒤 7자리 : " + jumin[7:]) # 끝 인덱스를 생략하면 문자열의 끝까지 가져옵니다.

print("뒤 7자리 (뒤에서부터) : " + jumin[-7:]) 
# 음수 인덱스를 사용하여 문자열의 끝에서부터 접근할 수도 있습니다.
