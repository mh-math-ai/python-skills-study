# 함수(function) 
# 함수는 코드를 재사용하기 위해 만들어진 것이다.
# 함수는 반복되는 작업을 한 번에 실행하고, 여러 개의 인자를 넣어서 사용할 수 있다.
# 함수는 값을 리턴(return)해주기도 한다.
# 함수는 여러 개의 인자를 받아서 사용하고, 여러 개의 값을 리턴해준다.
# 함수는 입력과 출력이 있어야 한다.

def open_account():
    print("새로운 계좌가 생성되었습니다.")

# 함수는 정의만 되고 실행은 안된다.
# 함수를 실행하기 위해서는 호출(call)을 해야한다.
open_account() # 함수를 실행시킴
# 함수의 이름은 대소문자를 구별하지 않는다.

 